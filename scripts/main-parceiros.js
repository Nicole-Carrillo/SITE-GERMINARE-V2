function abrirJbs() {
  document.getElementById('jbsinfo').style.display = "block";
}

document.addEventListener('mouseup', function(e) {
  let container = document.getElementById('jbsinfo');
  if (!container.contains(e.target)) {
    container.style.display = 'none';
  }
});




